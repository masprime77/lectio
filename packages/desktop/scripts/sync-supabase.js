'use strict';
// Vendor the Supabase browser bundle + renderer config next to index.html.
//
// The renderer runs with contextIsolation:true / nodeIntegration:false, so it
// can only pull supabase-js in via a <script src> URL relative to index.html —
// it can't require() the package or read process.env. Mirroring
// scripts/sync-core.js, this copies the UMD build to a stable sibling
// (packages/desktop/supabase.js) and writes packages/desktop/supabase-config.js,
// which exposes the project URL + publishable (anon) key as window.LECTIO_SUPABASE
// for supabase-client.js.
//
// The key is NOT secret-secret (the shipped apps embed it), but the repo keeps
// it out of git: the mobile app reads it from a git-ignored .env, so the desktop
// does the same — the URL/key are read from the environment
// (EXPO_PUBLIC_SUPABASE_URL / EXPO_PUBLIC_SUPABASE_PUBLISHABLE_KEY), falling back
// to packages/mobile/.env so a dev who already configured mobile gets them for
// free. Both outputs are git-ignored and regenerated on prestart/predev/prebuild.
const fs = require('fs');
const path = require('path');

const desktopDir = path.join(__dirname, '..');

// 1) Vendor the supabase-js UMD bundle (exposes window.supabase.createClient).
const umdSrc = require.resolve('@supabase/supabase-js/dist/umd/supabase.js');
fs.copyFileSync(umdSrc, path.join(desktopDir, 'supabase.js'));

// 2) Resolve URL + anon key from env, falling back to packages/mobile/.env.
function fromEnvFile() {
  const out = {};
  try {
    const text = fs.readFileSync(path.join(desktopDir, '..', 'mobile', '.env'), 'utf8');
    for (const line of text.split(/\r?\n/)) {
      const m = line.match(/^\s*([A-Za-z0-9_]+)\s*=\s*(.*)\s*$/);
      if (m) out[m[1]] = m[2].replace(/^["']|["']$/g, '');
    }
  } catch (e) {
    /* no .env — fall through to env vars / empty config */
  }
  return out;
}

const envFile = fromEnvFile();
const url = process.env.EXPO_PUBLIC_SUPABASE_URL || envFile.EXPO_PUBLIC_SUPABASE_URL || '';
const anonKey =
  process.env.EXPO_PUBLIC_SUPABASE_PUBLISHABLE_KEY ||
  envFile.EXPO_PUBLIC_SUPABASE_PUBLISHABLE_KEY ||
  '';

if (!url || !anonKey) {
  console.warn(
    '[sync-supabase] No Supabase URL/key found (env or packages/mobile/.env). ' +
      'Writing an empty config — sign-in will be unavailable until it is set.'
  );
}

// 3) Write the git-ignored config the renderer reads.
const config =
  '// AUTO-GENERATED by scripts/sync-supabase.js — do not edit or commit.\n' +
  '// Holds the Supabase project URL + publishable (anon) key for the renderer.\n' +
  'window.LECTIO_SUPABASE = ' +
  JSON.stringify({ url, anonKey }, null, 2) +
  ';\n';
fs.writeFileSync(path.join(desktopDir, 'supabase-config.js'), config);
